.data
asize0:  .word    100, 200, 300, 400, 500, 600
asize1:  .word	  700, 800, 900, 1000, 1100, 1200
.text

.globl main

main: 
	addi $s1, $0, $s0	# s1 = 0
	addi $t3, $0, 150	# t3 = 150
	addi $t4, $0, 50    # t4 = 50

	addi $a0, $0, 4		# a0 = 4
	addi $a0, $a0, 4	# a0 = 8
	lw $s2, 4($a0)		# s2 = 400


	lw $t0, 4($a0)		# t0 = 400
	sub $t2, $t3, $t4   # t2 = 100
	beq $t3, $t0, loop  # 400 != 150, branch not taken
	


	addi $s4, $s1, 4	#s4 = 4
	sw $s4, 12($s2)		#s4 = 500


	add $t1, $0, $t3   # t1 = t3 = 150
	sub $t2, $t3, $t4   # t2 = 150 + 50 = 200
	beq $t1, $t3, loop  # 150 == 150, branch should be taken
	
	#check to see if loop happened
	addi $t1, $0, -1
	
loop:
	j loop



